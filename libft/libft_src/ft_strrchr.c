#include "../include/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		size;
	char	*p;

	size = ft_strlen(s);
	p = (char *) s + size;
	while (size >= 0)
	{
		if (*p == (char) c)
			return (p);
		size --;
		p --;
	}
	return (0);
}
